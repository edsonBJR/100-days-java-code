package br.com.edsonbjr.datatypes;

public class FloatPointOperations {

    /*
    Floating-Point Operations:
    Write a Java program that declares two double variables,
    assigns them values, and then performs addition, subtraction,
    multiplication, and division operations on them. Print the results.
     */

    double a = 1.234;
    double b = 2.987;

    public FloatPointOperations() {}

    public double addition(){
        return a + b;
    }
    public double subtraction(){
        return a - b;
    }
    public double multiplication(){
        return a * b;
    }
    public double division(){
        return a / b;
    }
}
